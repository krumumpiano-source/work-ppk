def get_staff_dict():
    """Return dict: name -> {department, position, admin_role}"""
    result = {}
    for name, dept, pos, admin in STAFF_LIST:
        result[name] = {
            "department": dept,
            "position": pos,
            "admin_role": admin,
        }
    return result


def get_all_names():
    """Return set of all staff names."""
    return {s[0] for s in STAFF_LIST}


def get_all_lastnames():
    """Return set of all staff last names (for fuzzy matching)."""
    return {s[0].split()[-1] for s in STAFF_LIST}


def get_departments():
    """Return sorted list of unique departments."""
    return sorted(set(s[1] for s in STAFF_LIST))
